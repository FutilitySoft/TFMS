<P align=center><B><FONT face="Arial" color=#FF0000>Logging In</A></U></FONT></B></P>
<P align=left><font face="Arial">&nbsp;&nbsp; </font> 
<FONT face="Arial" color=#ffffff>The first step in doing any task in the 
Commanding Officer's Area is logging in. To do this, select CO's Lounge on the 
menu. On the page that is accessed, enter your username and password in the 
indicated fields. If you 
do not remember your password, email 
<a href="mailto:tf86@bravofleet.org?subject=TF86 Logon Problems">Admiral 
Wolfe</a>so that he can retrieve it 
for you. Once the fields are filled, click Login. This will bring you to the 
main page.</FONT></P>
<HR>

<P align=center><FONT face="Arial" color=#FF0000><B>Adding/Deleting Crew</A></B></U></FONT></P>
<P align=left><font face="Arial">&nbsp;&nbsp; </font> 
<FONT face="Arial" color=#ffffff>The CO's 
Lounge contains a manifest of your crew which must be kept up to date. This 
manifest will be applied to your monthly reports. <BR>&nbsp;&nbsp;&nbsp; To add 
a crew member to the manifest:&nbsp; (This includes you, the CO) At the bottom 
of the crew listing, fill out the three available fields. The first field is the 
character's name. Second field is their position. Third is their email address. 
Do NOT type in a rank, the software does that automatically. Click the [Add] link. 
This will place your crewmember into your manifest, and assigns them a rank of 
cadet. You need to click on [Edit] and update their rank. <BR>&nbsp;&nbsp;&nbsp; 
To update their rank, select the correct color and rank from the drop down box. <B>NOTE:&nbsp; </B>Make sure to use 
the appropriate color.&nbsp; The colors are as follows.</FONT></P>
<P align=left><FONT face="Arial">(Starfighter) - Starfleet Fighter 
Pilots (Carrier)<BR>(Grey) - Intelligence Officers<BR></FONT>
<FONT face="Arial" color=#ffffff>(Red) - Command, Flight 
Control, Strategic Operations<BR>(Purple) - Diplomatic Corps<BR>(Blue) - 
Medical, Science, Counseling<BR>(Green) - Marine Corps<BR>Cadet / Civilian - Use for Cadets and 
Civilians<BR>(Gold) - Operations, Tactical / Security, Engineering</FONT></P>
<P align=left><FONT face="Arial" color=#ffffff><BR>&nbsp;&nbsp;&nbsp; Once the 
crewman's new information is entered, click Edit again. <BR>&nbsp;&nbsp;&nbsp; To delete a 
crewman from the manifest, simply click the Delete button at the end of that 
crewman's row on the main page. </FONT></P>
<HR>

<P align=center><FONT face="Arial" color=#FF0000><B>Editing Crew Information</A></B></U></FONT></P>
<P align=left><font face="Arial">&nbsp;&nbsp;&nbsp;</font><FONT face="Arial" color=#ffffff> The 
edit information already entered for a crewman, click the edit button at the end 
of that crewman's row on the main page. This will return you to a page exactly 
like the one you originally used to enter that crewman's information. Simply 
edit what you wish, and click Edit.</FONT></P>
<HR>

<P align=center><FONT face="Arial" color=#FF0000><B>Editing CO Information</A></B></U></FONT></P>
<P align=left><font face="Arial">&nbsp;&nbsp;&nbsp; This is where you can update 
your own information, such as email address and password. This will not change 
your entry in the crew manifest though. Be sure to update it there as well.</font></P>
<HR>

<P align=center><FONT face="Arial" color=#FF0000><B>Editing Sim 
Information</A></U></B></FONT></P>
<P align=left><font face="Arial">&nbsp;&nbsp;&nbsp;</font><FONT face="Arial" color=#ffffff> Much 
of the information displayed with your ship/station's banner on the website is 
controllable by you.&nbsp; The things you can edit are Mission Title, XO, MCO, 
COB/CMC, website, # of Posts, and # of awards.<BR>&nbsp;&nbsp;&nbsp; Just update what ever information 
you need to, then click edit after that field.</FONT></P>
<HR>

<P align=center><FONT face="Arial" color=#FF0000><B>Assigning XO</A>, MCO, COB/CMC</U></B></FONT></P>
<P align=left><FONT face="Arial" color=#ffffff>&nbsp;&nbsp;&nbsp; On 
the Task Force website, your sim's Executive Officer, MCO, COB/CMC are displayed below 
yourself. What member of your crew is displayed there is controllable by 
you.<BR>&nbsp;&nbsp;&nbsp; Before you can assign a crew member to the one of 
these 
positions, they must first be entered into the crew manifest on the TF site. For instructions on doing 
this, see </FONT><FONT face="Arial" color=#FF0000>Adding/Deleting 
Crew</FONT><FONT face="Arial" color=#ffffff>.<BR>&nbsp;&nbsp;&nbsp; 
Once the XO, MCO, and COB are added to the manifest, select the crewmember to 
fill the needed postion, and then click the [Assign] button next to the entry. 
The MCO and COB position has an option in the drop down box for ships that do 
not want these positions on their ship. Just select No MCO (OR COB) Postion - Do 
Not Display. This can always be changed at a later time.</FONT></P>
